CREATE TABLE IF NOT EXISTS PENDING_IMAGE (
    ID bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    URL TEXT NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS TWEET_IMAGE (
    ID bigint NOT NULL,
    SIZE int NOT NULL,
    TYPE TEXT NOT NULL,
    WIDTH int NOT NULL,
    HEIGHT int NOT NULL,
    PRIMARY KEY (ID)
);

